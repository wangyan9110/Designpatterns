package org.yywang;

/**
 * @author wangyan
 * @date 2014-12-19 21:21
 */
public class Position {

    private float x;

    private float y;

    public Position(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
